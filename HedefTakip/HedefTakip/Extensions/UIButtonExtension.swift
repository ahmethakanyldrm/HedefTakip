import Foundation
import UIKit

extension UIButton {
    func unSelectedButtonBackgroundColor(){
        self.backgroundColor = #colorLiteral(red: 0.0338188, green: 0.407009691, blue: 0.6288487911, alpha: 1)
    }
    
    func selectedButtonBackgroundColor(){
        self.backgroundColor = #colorLiteral(red: 0.01533038449, green: 0.6499723196, blue: 1, alpha: 1)
    }
    
    
}
